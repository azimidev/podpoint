#!/bin/bash

# ------------------------------------------------
# Prepare project
# ------------------------------------------------
composer install

# ------------------------------------------------
# Copy .env
# ------------------------------------------------
cp .env.example .env

# ------------------------------------------------
# Generate Key
# ------------------------------------------------
php artisan key:generate

# ------------------------------------------------
# Migrate the database
# ------------------------------------------------
php artisan migrate
php artisan db:seed

# ------------------------------------------------
# Install node dependancies
# ------------------------------------------------
cd front
yarn
yarn build

cd ..
php artisan serve
